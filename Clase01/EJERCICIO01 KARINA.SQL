--DESARROLLAR UNA FUNCION PARA CALCULAR EL PROMEDIO
--DE UN ALUMNO DE PL/SQL, SON 4 NOTAS.

CREATE OR REPLACE FUNCTION scott.FNPROMEDIO
( NOTA1 NUMBER, NOTA2 NUMBER, NOTA3 NUMBER, NOTA4 NUMBER) 
RETURN NUMBER
IS 
    PROMEDIO NUMBER;
BEGIN
    PROMEDIO := (NOTA1 + NOTA2 + NOTA3 + NOTA4)/4;
    RETURN PROMEDIO;
END;
/


SELECT scott.FNPROMEDIO (12.5,10.3,15,15) FROM DUAL;
