SET SERVEROUTPUT ON

CREATE OR REPLACE TRIGGER SCOTT.TR_FOR_EACH_ROW
AFTER UPDATE ON SCOTT.EMP 
FOR EACH ROW
BEGIN 
 
  DBMS_OUTPUT.PUT_LINE( 'CODIGO: ' || :NEW.EMPNO ); 
  DBMS_OUTPUT.PUT_LINE( 'SALARIO OLD: ' || :OLD.SAL ); 
  DBMS_OUTPUT.PUT_LINE( 'SALARIO NEW: ' || :NEW.SAL ); 
  DBMS_OUTPUT.PUT_LINE( '------------------------------' ); 
 
END TR_FOR_EACH_ROW; 
/

